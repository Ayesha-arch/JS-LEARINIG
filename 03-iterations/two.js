// while loop

let index = 1
while (index <= 10) {
    console.log(`The value of index is ${index}`);
    index = index + 2
}

let MyArray = ["one" , "two" , "three"]
let arr = 0
while (arr < MyArray.length) {
    console.log(`The value is: ${MyArray[arr]}`);
    arr = arr + 1
}

// do-while loop

let score = 11
do {
    console.log(`Score is: ${score}`);
    score++
    
} while (score <= 10);